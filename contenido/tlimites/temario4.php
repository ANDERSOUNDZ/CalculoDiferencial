      <h2 class="r">Potencias de números enteros</h2></div>
      <p class="r"><span class="num-ej">1</span>Potencia de número entero positivo</p>
      <p>Para determinar el signo de la potencia de un número entero tendremos en cuenta que:</p>
      	  <p><span class="num-subej">1</span>Las potencias de <span class="v">exponente par</span> son siempre <span class="r">positivas</span>.</p>
   	  <div class="formula">
        <p class="b">(<span class="r">+</span>)<sup>par</sup> = <span class="r">+</span></p>
        <p class="b">(<span class="r">−</span>)<sup>par</sup> = <span class="r">+</span></p>
      </div><!-- fin div.formula-->
     
    <div class="pop-ej">
     <div class="float1"><span class="g border">Ejemplo:</span></div><!--fin.float1-->
     <div class="float2">
     <p class="guion-r">2<sup>6 </sup> = 64</p>
     <p class="guion-r" style="width:98%;">(−2)<sup>6 </sup> = 64</p>
      </div>
     <!-- fin div.float2-->
     </div><!--fin.pop-ej-->

      <p><span class="num-subej">2</span> Las potencias de <span class="v">exponente impar</span> tienen el <span class="r">mismo signo de la base</span>.</p>
      <div class="formula">
      <p class="b">(<span class="r">+</span>)<sup>impar</sup> = <span class="r">+</span></p>
        <p class="b">(<span class="r">−</span>)<sup>impar</sup> = <span class="r">−</span></p>
      </div><!-- fin div.formula-->

	<div class="pop-ej">
     <div class="float1"><span class="g border">Ejemplo:</span></div><!--fin.float1-->
     <div class="float2">
     <p class="guion-r">2³ = 8</p>
     <p class="guion-r" style="width:98%;">(−2)³ = −8</p>
     </div><!-- fin div.float2-->
    </div><!--fin.pop-ej-->
