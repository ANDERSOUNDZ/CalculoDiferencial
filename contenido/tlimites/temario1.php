    <b>¿Qué es el límite de un función?</b>
    <img src="archivos/img/A4.png" border="1" width="400" height="300">   
        <p>
          
        El límite de la función f(x) en el punto x0, es el valor al que se acercan las imágenes (las y) cuando los originales (las x) se acercan al valor x0. Es decir el valor al que tienden las imágenes cuando los originales tienden a x0.
    </p>
    </div>
</div>
<div class="bg3 col">
    <br>
    <div class="center-block">    
        <p>
        <div class="center-block" align="center">   
        <h1>Propiedades de los límites</h1><br>
    </div>
Dadas dos funciones f(x) y g(x) que tienen límite en un punto a, se cumplen las siguientes propiedades:
<br><br>
·         El límite de la suma de ambas funciones es igual a la suma de los límites. <br>
·         El límite de la diferencia se calcula como la diferencia de los límites. <br>
·         El límite del producto de las funciones es igual al producto de sus límites. <br>
·         El límite del cociente entre ambas funciones es igual al cociente entre los límites, siempre y cuando el límite del denominador sea distinto de cero. <br>
·         El límite del producto de una constante por una función viene determinado por la multiplicación de la constante por el límite de la función. <br>   
        </p>
