      <b><h2>Teoría del criterio de la segunda derivada :</h2></b><br>

      <img src="https://sites.google.com/site/usilmatematicatello/_/rsrc/1447055738789/criterio-de-la-primera-y-segunda-derivada/55555.png?height=275&width=400" border="1" width="400" height="300"> 

     
<br><br>
El Criterio o prueba de la segunda derivada es un teorema o método del cálculo matemático en el que se utiliza la segunda derivada para efectuar una prueba simple correspondiente a los máximos y mínimos relativos. <br><br>

Se basa en el hecho de que si la gráfica de una función f es convexa en un intervalo abierto que contiene a c, y f '(c)=0, f(c) debe ser un mínimo relativo de f. De manera similar, si la gráfica de una función es cóncava hacia abajo en un intervalo abierto que contiene a c y f '(c) = 0, f(c) debe ser un máximo relativo de f. <br><br>

Sea f una función tal que f '(x) = 0 y la segunda derivada de  f existe en un intervalo abierto que contiene a x <br><br>

    Si f ''(x) < 0, entonces f tiene un máximo relativo en (x, f(x)). <br>
    Si f ''(x) > 0, entonces f tiene un mínimo relativo en (x, f(x)). <br>

    Si f ''(x) = 0, entonces el criterio falla. Esto es, f quizás tenga un máximo relativo en x, un mínimo relativo en (x, f(x)) o ninguno de los dos. Tomar como ejemplo la función f(x) = x³. En tales casos, se puede utilizar el criterio de la primera derivada o el criterio de la tercera derivada.


