Derivada algebraica <br><br>

La derivada es la pendiente de una recta tangente a la función de un determinado punto, por lo que la función tiene que estar en ese punto donde se podrá trazar una recta que es tangente en él.
Derivada del producto <br><br>

<b>La derivada de un producto </b> <br> en dos funciones es similar al primer factor multiplicado por la derivada del segundo sumándole el segundo factor y multiplicándolo por la derivada del primero. Ejemplo:f(x)=u.v entonces f’(x)=u’.v+u.v’
Derivada del cociente <br><br>

<b>La derivada que tiene un cociente</b><br> en dos funciones es similar a la derivada que tiene el numerador multiplicada por el denominador y menos la derivada que tiene el denominador por el numerador, dividida entre el cuadrado que tiene el denominador. Ejemplo: si f(x)=u/v <br>

Entonces f’(x)=u’.v –u.v’
